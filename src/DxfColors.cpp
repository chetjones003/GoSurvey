inline void UnpackRgb(uint32_t p, int* r, int* g, int* b) {
  *r = static_cast<int>((p >> 16) & 0xFF);
  *g = static_cast<int>((p >> 8) & 0xFF);
  *b = static_cast<int>(p & 0xFF);
}

inline int SqDist(uint32_t a, uint32_t b) {
  int ar, ag, ab, br, bg, bb;
  UnpackRgb(a, &ar, &ag, &ab);
  UnpackRgb(b, &br, &bg, &bb);
  const int dr = ar - br;
  const int dg = ag - bg;
  const int db = ab - bb;
  return dr * dr + dg * dg + db * db;
}
