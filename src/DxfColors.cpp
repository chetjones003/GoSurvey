uint32_t DxfRgbPackedFromAci(int aci) {
  if (aci < 1 || aci > 255)
    return kAcad2020MsRgb[7]; // default white in palette slot 7
  return kAcad2020MsRgb[aci];
}

int DxfNearestAciFromRgbPacked(uint32_t rgbPacked) {
  rgbPacked &= 0xFFFFFFu;
  int best = 7;
  int bestD = SqDist(rgbPacked, kAcad2020MsRgb[7]);
  for (int i = 1; i <= 255; ++i) {
    const int d = SqDist(rgbPacked, kAcad2020MsRgb[i]);
    if (d < bestD) {
      bestD = d;
      best = i;
    }
  }
  return best;
}

void DxfRgbPackedToHex(uint32_t rgbPacked, char* out, size_t cap) {
  if (!out || cap < 8)
    return;
  rgbPacked &= 0xFFFFFFu;
  std::snprintf(out, cap, "#%06X", static_cast<unsigned>(rgbPacked));
}
